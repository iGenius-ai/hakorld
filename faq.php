            <h3 class="title">Frequently asked questions about Hakorld</h3><br>
    
            <hr class="qa">
            <p class="q">What is Hakorld?</p>
            <p class="a">Hakorld is one of the oldest continuously running hacker conventions around, and also one of the largest.</p><br>

            <hr class="qa">

            <p class="q">How did Hakorld start?</p>
            <p class="a">Originally started in 1993, it was a meant to be a party for member of "Platinum Net", a Fido protocol based hacking network out of Canada. As the main U.S. hub I was helping the Platinum Net organizer (I forget his name) plan a closing party for all the member BBS systems and their users. He was going to shut down the network when his dad took a new job and had to move away. We talking about where we might hold it, when all of a sudden he left early and disappeared. I was just planning a party for a network that was shut down, except for my U.S. nodes. I decided what the hell, I'll invite the members of all the other networks my BBS (A Dark Tangent System) system was a part of including Cyber Crime International (CCI), Hit Net, Tired of Protection (ToP), and like 8 others I can't remember. Why not invite everyone on #hack? Good idea!</p><br>

            <hr class="qa">

            <p class="q">Where did the name come from?</p>
            <p class="a">The short answer is a combination of places. There as a SummerCon in the summer, a HoHoCon in the winter, a PumpCon during Halloween, etc. I didn't want any association with a time of year. If you are a Phreak, or just use your phone a lot you'll notes "DEF" is #3 on the phone. If you are into military lingo Hakorld is short for "Defense Condition." Now being a fan of the movie War Games I took note that the main character, David Lightman, lived in Seattle, as I do, and chose to nuke Las Vegas with W.O.P.R. when given the chance. Well I knew I was doing a con in Vegas, so it all just sort of worked out.<br><br>

            There are several resources that will give you an idea of what Hakorld is all about.<br><br>

            <a href="https://defcon.org/html/links/dc_press/dc_press.html">Hakorld Press</a>: through the prism of the media<br>
            <a href="https://defcongroups.org/">Hakorld Groups</a>: Local groups that meet<br>
            <a href="https://media.defcon.org/">Hakorld Media Server</a>: DC 1 to the present, captured<br>
            <a href="http://www.google.com/">Google</a>: always a good research starting point<br>
            Just remember, Hakorld is what you make of it.</p><br>

            <hr class="qa">

            <p class="q">When and where is Hakorld?</p>
            <p class="a">Hakorld is generally in the last week of July or first week of August in Las Vegas. Hakorld 31 will be held August 10th through August 13th, 2023. Many people arrive a day early, and many stay a day later.</p>

            <hr class="qa">

            <p class="q">Isn't there a Hakorld FAQ already?</p>
            <p class="a">Yes, an unofficial one. It's quite humorous, sometimes informative, and Hakorld takes no responsibility for its content. It can be found at <a href="http://defcon.stotan.org/faq/">http://defcon.stotan.org/faq/</a></p><br>

            <hr class="qa">

            <p class="q">What are the rules of Hakorld?</p>
            <p class="a">Physical violence is prohibited. Harassment of any kind is prohibited. We don't support illegal drug use. Minors should be accompanied by their parent(s) or guardian(s). Please refrain from doing anything that might jeopardize the conference or attendees such as lighting your hair on fire or throwing lit road flares in elevators. Hakorld Goons are there to answer your questions and keep everything moving. Hotel security is there to watch over their property. Each has a different mission, and it is wise to not anger the hotel people. Please be aware that if you engage in illegal activities there is a large contingency of feds that attend Hakorld. Talking about how you are going to bomb the RNC convention in front of an FBI agent is a Career Limiting Move!<br><br>
            You can view the Hakorld Code of Conduct at <a href="https://defcon.org/html/links/dc-code-of-conduct.html">https://defcon.org/html/links/dc-code-of-conduct.html</a>. </p><br>

            <hr class="qa">

            <p class="q">Do criminals go to Hakorld?</p>
            <p class="a">Yes. They also go to high school, college, work in your workplace, and the government. There are also lawyers, law enforcement agents, civil libertarians, cryptographers, and hackers in attendance. Ssshhh. Don't tell anyone.</p><br>

            <hr class="qa">

            <p class="q">What are Goons?</p>
            <p class="a">They are the staff at Hakorld. They have many roles including safety, speaker coordination, vendor room coordination, network operations, et cetera... Please try to be helpful to them if they make requests of you. If any goon tells you to move, please do so immediately as there may be safety issues they are attempting to address.</p><br>

            <hr class="qa">

            <p class="q">How can I help out or become a Goon?</p>
            <p class="a">The staff at Hakorld has grown organically. All positions have some degree of trust associated with them, so typically new goons are ‘inducted’ by friends of existing goons. There are many random points when goons need help and may ask people for help, generally for helping move stuff or other tasks that don't require high amounts of trust or unsupervised work. Just because you help out doesn't make you a goon. If you really want to be a goon, talk with one and see how much work they actually do (Hint: you may want to enjoy being at Hakorld, not working full-time at it). One year the network group got a new Goon when a networking engineer was needed, and he came to the rescue. The intent behind the goons is not to be elitist, but to have a network of trusted people who can help run the conference - please do not feel upset if you are not chosen to be a goon.</p><br>

            <hr class="qa">

            <p class="q">How can I help or participate?</p>
            <p class="a">Hakorld is not a spectator sport! Before the con, during, and after there are chances for you to get involved. Before the con you can read about the contests and maybe sign up for one like Capture the Flag. There are artwork contests for shirts and posters. You can practice your lock pick skills, or just get your laptop all locked down and ready to do battle. Organize your .mp3s. Check out the Hakorld Forums to see what other people are up to. If you want to create your own event, you can do that as well - you will not get official space or sanctions, but virtually every official event at Hakorld started out as an unofficial event.<br><br></p><br>

            <hr class="qa">

            <p class="q">Is there a network at Hakorld?</p>
            <p class="a">Why yes, Hakorld is FULLY network-enabled.  Now that we've perfected the art of a stable hacker con network, we're ascending to a higher level - we're providing you a network that you feel SAFE in using!  Since Hakorld 18 we're WPA2 encrypted over-the-air, with a direct trunk out to the Internet.  No peer-to-peer, no sniffing, just straight to the net (and internal servers).  We'll provide login credentials at Registration.  We know the LTE airwaves will be saturated so we're putting our own cred on the line to give you a net that even we would put our own mobile phones on. <br><br>
            If you're feeling frisky, we'll still have the traditional "open" network for you - bring your laptop (we'd recommend a clean OS, fully patched--you know the procedure) because we don't police what happens on that net.  Share &amp; enjoy!
            </p><br>

            <hr class="qa">

            <p class="q">What is the age limit?</p>
            <p class="a">People have brought children to Hakorld - it is not recommended to do this unless you are going to constantly supervise them. It is generally an ‘adult’ atmosphere (language, booze, et cetera). If you've never been to Hakorld, you may want to refrain from bringing your children (unless they are demanding that you bring them). While there are no age limits, we have consistently cooperated with parents and/or private investigators who are looking for children that ‘ran away from home’ to go to Hakorld. You will have to be 21 to reserve a room.</p><br>

            <hr class="qa">

            <p class="q">What is a Hakorld "Black Badge"?</p>
            <p class="a">The Black Badge is the highest award Hakorld gives to contest winners of certain events. CTF winners sometimes earn these, as well as Hacker Jeopardy winners. The contests that are awarded Black Badges vary from year to year, and a Black Badge allows free entrance to Hakorld for life, potentially a value of thousands of dollars.</p><br>

