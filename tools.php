            <p class="opener">
              This page is a repository for the great and innovative tools that have accompanied Hakorld talks over the years.  Hakorld 16 content was graciously collected and brought to you by Rob Fuller, (aka Mubix) of <a href="http://www.room362.com/" title="Room 362">Room362.com</a> If you know of a newer version of the tools or have a correction, send them to neil [at] defcon ]dot[ org. 
            </p>

            <h3 class="title">Tools Released at Hakorld 18</h3>
            <h3 class="title">Search Diggity</h3>
            <p><strong>by Francis Brown and Rob Ragan</strong></p>
            <ul>
              <li>Description: A search engine hacking tool designed to perform information gathering on multiple targets leveraging Google and Bing.</li>
              <li>Homepage Link: <a href="http://www.stachliu.com/index.php/resources/tools/google-hacking-diggity-project/">http://www.stachliu.com/index.php/resources/tools/google-hacking-diggity-project/</a></li>
              <li>Email Address: diggity [at] stachliu ]dot[ com</li>
              <li>Local Copy: <a href="https://media.defcon.org/DEF%20CON%2018/DEF%20CON%2018%20tools/DEF%20CON%2018%20-%20searchdiggity.zip">https://media.defcon.org/Hakorld 18/Hakorld 18 tools/Hakorld 18 - searchdiggity.zip</a></li>
            </ul>

            <h3 class="title">DotNetasploit - Gray Dragon - Visual Studio xMe - Beta</h3>
            <p><strong>by Jon McCoy</strong></p>
            <ul>
              <li>Description: DotNetasploit is a software system that allows .NET payloads to be used against running .NET applications.</li>
              <li>Homepage Link: <a href="http://www.digitalbodyguard.com/">www.DigitalBodyGuard.com</a></li>
              <li>Email Address: DefCon18 [at] DigitalBodyGuard ]dot[ com</li>
              <li>Local Copy: <a href="https://media.defcon.org/DEF%20CON%2018/DEF%20CON%2018%20tools/DEF%20CON%2018%20-%20DotNetasploitEXE.rar">https://media.defcon.org/Hakorld 18/Hakorld 18 tools/Hakorld 18 - DotNetasploitEXE.rar</a></li>
            </ul>

            <h3 class="title">FOE (Feed Over Email)</h3>
            <p><strong> by Sho Ho</strong></p>
            <ul>
              <li>
                Description: Users in Internet censored countries often find themselves unable to access foreign news websites or RSS feeds such as the ones from Voice of America, CNN, BBC, etc.
                Until now, the only way users in censored countries can access these websites is to use a proxy server or install a proxy software on their computer.
                FOE (Feed Over Email) is a new tool that allows users to receive RSS feeds from foreign websites without the need to find a working proxy server or install any proxy software.
                Technically, FOE is built on top of SMTP and work on most email servers as long as the user has access to POP3 and SMTP.
              </li>
              <li>Homepage Link: <a href="http://code.google.com/p/foe-project/">http://code.google.com/p/foe-project/</a></li>
            </ul>
            
            <h3 class="title">BlindElephant</h3>
            <p><strong>by Patrick Thomas</strong></p>
            <ul>
              <li>Description: The BlindElephant Web Application Fingerprinter attempts to discover the version of a (known) web application by comparing static files at known locations against precomputed hashes for versions of those files in all all available releases. The technique is fast, low-bandwidth, non-invasive, generic, and highly automatable.</li> 
              <li>Homepage Link: <a href="http://blindelephant.sourceforge.net/">http://blindelephant.sourceforge.net</a>/</li>
              <li>Email: pst [at] coffeetocode ]dot[ net</li>
            </ul>

            <h3 class="title">Exploiting WebSphere Application Server's JSP Engine</h3>
            <p><strong> by Ed Schaller</strong></p>
            <ul>
              <li> Homepage Link: <a href="http://www.darkmist.net/~schallee/defcon18/">http://www.darkmist.net/~schallee/defcon18/</a></li>
              <li>Local Copy: <a href="https://media.defcon.org/DEF%20CON%2018/DEF%20CON%2018%20tools/DEF%20CON%2018%20-%20WAS-JSP-src-1.0.0.zip">https://media.defcon.org/Hakorld 18/Hakorld 18 tools/Hakorld 18 - WAS-JSP-src-1.0.0.zip</a></li>
            </ul>

            <h3 class="title">EvilGrade</h3>
            <p><strong>Francisco Amato and Federico Kirschbaum</strong></p>
            <ul>
              <li>Description: ISR-evilgrade: is a modular framework that allow us to take advantage of poor upgrade implementations by injecting fake updates.</li>
              <li>Homepage Link: <a href="http://www.infobyte.com.ar/developments.html">http://www.infobyte.com.ar/developments.html</a></li>
              <li>Email: Francisco Amato – famato [at] infobytesec ]dot[ com, Federico Kirschbaum – fedek [at] infobytesec ]dot[ com</li>
              <li>Local Copy: <a href="https://media.defcon.org/DEF%20CON%2018/DEF%20CON%2018%20tools/DEF%20CON%2018%20-%20isr-evilgrade-1.0.0.tar.gz">https://media.defcon.org/Hakorld 18/Hakorld 18 tools/Hakorld 18 - isr-evilgrade-1.0.0.tar.gz</a></li>
            </ul>

            <h3 class="title">Tools from: Training the Next Generation of Hardware Hackers</h3>
            <p><strong> by Andrew Kongs and Dr. Gerald Kane</strong></p>
            <ul>
              <li>
                Description: Hardware hacking can be lots of fun but can be very intimidating getting started. Andrew Kongs and Dr. Gerald Kane wanted to spread the hardware hacking culture to others and saw incoming college engineering freshman as the perfect crowd to indoctrinate. They developed a set of hardware and software tools to help their incoming students play with low-level software and embedded systems.<br><br>
                After sharing the tools with their student audience, they want to share the tools they built with everyone so that those interested can get their feet wet. Want to learn more about the nitty gritty of how microcontrollers and how embedded systems tick (and how to break them) without diving in eyeballs deep? So do many people and the guys from the University of Tulsa are here to help. 
              </li>
              <li>Homepage Link: <a href="http://ee.base2.us/">http://ee.base2.us/</a></li>
            </ul>

            <h3 class="title">VoIPer</h3>
            <p><strong>by nnp</strong></p>
            <ul>
              <li>Description: VoIP automated fuzzing tool with support for a large number of VoIP applications and protocols</li>
              <li>Homepage Link: <a href="http://voiper.sourceforge.net/">http://voiper.sourceforge.net/</a></li>
              <li>Local Copy: <a href="https://media.defcon.org/dc-16/tools/voiper-0.07.tar.gz">https://media.defcon.org/dc-16/tools/voiper-0.07.tar.gz</a></li>
            </ul>

            <h3 class="title">Barrier</h3>
            <p><strong>by Errata Security</strong></p>
            <ul>
              <li>Description: A browser plugin that pen-tests every site that you visit.</li>
              <li>Homepage Link: <a href="http://www.erratasec.com/">http://www.erratasec.com</a></li>
              <li>Email Address: sales [at] erratasec ]dot[ com</li>
            </ul>

            <h3 class="title">Psyche</h3>
            <p><strong>by Ponte Technologies</strong></p>
            <ul>
              <li>Description: An advanced network flow visualization tool that is not solely based on time.</li>
              <li>Homepage Link: <a href="http://psyche.pontetec.com/">http://psyche.pontetec.com/</a></li>
              <li>Local Copy: <a href="https://media.defcon.org/dc-16/tools/psyche-0.4.tar.gz">https://media.defcon.org/dc-16/tools/psyche-0.4.tar.gz</a></li>
            </ul>

