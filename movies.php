	    <h2>Movies</h2>
	    
	     <p class="opener">Far from exhaustive, this is a short list of films that have some hackish content and function as a fun starting point for exploring hacker cinema. Some of them are fun, some of them are meatier fare - the common link is the highlighting of the hacker mindset. If you have others you want to suggest, let us know. We'll keep the list growing. </p>

														<a name="ComputerReference"></a><h3 class="title">Feature Films</h3>
											
														<p>
														<a title="Hackers - poster"><img class="pullRight" src="assets/images/movies/hackers-poster.jpg"></a>
											            
											            <strong><a href="http://www.amazon.com/gp/product/B009950GAQ/ref=as_li_tl?ie=UTF8&amp;camp=1789&amp;creative=9325&amp;creativeASIN=B009950GAQ&amp;linkCode=as2&amp;tag=dc03f3-20&amp;linkId=OEBQTPBRFVUOFSFG">Hackers</a><img src="http://ir-na.amazon-adsystem.com/e/ir?t=dc03f3-20&amp;l=as2&amp;o=1&amp;a=B009950GAQ" width="1" height="1" border="0" alt="" style="border:none !important; margin:0px !important;"></strong><br>
											           
													Director: Iain Softley, 1995<br><br>
													IMDB Summary: A young boy is arrested by the U.S. Secret Service for writing a computer virus and is banned from using a computer until his 18th birthday. Years later, he and his new-found friends discover a plot to unleash a dangerous computer virus, but they must use their computer skills to find the evidence while being pursued by the Secret Service and the evil computer genius behind the virus. 
														</p>
														<hr>
														
														
														<p>
														<a title="Sneakers - poster"><img class="pullRight" src="assets/images/movies/sneakers-poster.jpg"></a>
											            
											            <strong><a href="http://www.amazon.com/gp/product/B000ID1J40/ref=as_li_tl?ie=UTF8&amp;camp=1789&amp;creative=9325&amp;creativeASIN=B000ID1J40&amp;linkCode=as2&amp;tag=dc03f3-20&amp;linkId=5IMO42GNSXV3MSHK">Sneakers</a><img src="http://ir-na.amazon-adsystem.com/e/ir?t=dc03f3-20&amp;l=as2&amp;o=1&amp;a=B000ID1J40" width="1" height="1" border="0" alt="" style="border:none !important; margin:0px !important;"></strong><br>
											            
											        
													Director: Phil Alden, 1992<br><br>
													IMDB Summary: Complex but lighthearted thriller about computers and cryptography, government and espionage, secrets and deception and betrayal.
														</p>
														
														<hr>
														
														<p>
														<a title="WarGames - poster"><img class="pullRight" src="assets/images/movies/wargames.jpg"></a>
											            
											            <strong><a href="http://www.amazon.com/gp/product/B0011EQBOS/ref=as_li_tl?ie=UTF8&amp;camp=1789&amp;creative=9325&amp;creativeASIN=B0011EQBOS&amp;linkCode=as2&amp;tag=dc03f3-20&amp;linkId=27EDKMMYM2ZMTT3K">WarGames</a><img src="http://ir-na.amazon-adsystem.com/e/ir?t=dc03f3-20&amp;l=as2&amp;o=1&amp;a=B0011EQBOS" width="1" height="1" border="0" alt="" style="border:none !important; margin:0px !important;"></strong><br>
											         
													
													Director: John Badham, 1983<br><br>
													IMDB Summary: A young man finds a back door into a military central computer in which reality is confused with game-playing, possibly starting World War III.
														</p>
														
														
														
														<hr>
														
														<p>
														<a title="Operation Takedown - poster"><img class="pullRight" src="assets/images/movies/takedown.jpg"></a>
											            
											            <strong><a href="http://www.amazon.com/gp/product/B0002L57YQ/ref=as_li_tl?ie=UTF8&amp;camp=1789&amp;creative=9325&amp;creativeASIN=B0002L57YQ&amp;linkCode=as2&amp;tag=dc03f3-20&amp;linkId=6GAMUFJQN4KCDQLF">Trackdown</a><img src="http://ir-na.amazon-adsystem.com/e/ir?t=dc03f3-20&amp;l=as2&amp;o=1&amp;a=B0002L57YQ" width="1" height="1" border="0" alt="" style="border:none !important; margin:0px !important;"></strong><br>
											         
													Director: Joe Chappelle, 2000<br><br>
													IMDB Summary: This film is based on the story of the capture of computer hacker "Kevin Mitnick". [quotes are actually in the IMDB summary].

														</p>
														
														
														
														<hr>
														
														<p>
														<a title="Colossus - poster"><img class="pullRight" src="assets/images/movies/colossus.jpg"></a>
											            
											            <strong><a href="http://www.amazon.com/gp/product/B0003JAOO0/ref=as_li_tl?ie=UTF8&amp;camp=1789&amp;creative=9325&amp;creativeASIN=B0003JAOO0&amp;linkCode=as2&amp;tag=dc03f3-20&amp;linkId=Y5UHIZG2HO6B6IBT">Colossus - The Forbin Project</a><img src="http://ir-na.amazon-adsystem.com/e/ir?t=dc03f3-20&amp;l=as2&amp;o=1&amp;a=B0003JAOO0" width="1" height="1" border="0" alt="" style="border:none !important; margin:0px !important;"></strong><br>
											         
													Director: Joseph Sargent, 1970<br><br>
													IMDB Summary: An artificially intelligent supercomputer is developed and activated, only to reveal that it has a sinister agenda of its own.


														</p>
														
														
														
														<hr>
														
														<p>
														<a title="Hardware - poster"><img class="pullRight" src="assets/images/movies/hardware.jpg"></a>
											          
											            <strong><a href="http://www.amazon.com/gp/product/B002E2QHAE/ref=as_li_tl?ie=UTF8&amp;camp=1789&amp;creative=9325&amp;creativeASIN=B002E2QHAE&amp;linkCode=as2&amp;tag=dc03f3-20&amp;linkId=MGTRJ73GR3JA6A77">Hardware </a><img src="http://ir-na.amazon-adsystem.com/e/ir?t=dc03f3-20&amp;l=as2&amp;o=1&amp;a=B002E2QHAE" width="1" height="1" border="0" alt="" style="border:none !important; margin:0px !important;"></strong><br>
											         
													Director: Richard Stanley, 1990<br><br>
													IMDB Summary: The head of a cyborg reactivates and rebuilds itself and goes on a violent rampage in a space marine's girlfriend's apartment.


														</p>
														
														
														
														<hr>
														
														<p>
														<a title="The Signal - poster"><img class="pullRight" src="assets/images/movies/signal.jpg"></a>
											            
											            <strong><a href="http://www.amazon.com/gp/product/B00NFDEM26/ref=as_li_tl?ie=UTF8&amp;camp=1789&amp;creative=9325&amp;creativeASIN=B00NFDEM26&amp;linkCode=as2&amp;tag=dc03f3-20&amp;linkId=OFIKORGP35EJST5S">The Signal</a><img src="http://ir-na.amazon-adsystem.com/e/ir?t=dc03f3-20&amp;l=as2&amp;o=1&amp;a=B00NFDEM26" width="1" height="1" border="0" alt="" style="border:none !important; margin:0px !important;"><br>
											         
													Director: William Eubank, 2014<br><br>
													IMDB Summary: On a road trip, Nic and two friends are drawn to an isolated area by a computer genius. When everything suddenly goes dark, Nic regains consciousness - only to find himself in a waking nightmare.


														</strong></p><strong>
														
														
														
														<hr>
														
														<p>
														<a title="Ghost in the Shell - poster"><img class="pullRight" src="assets/images/movies/ghostintheshell.jpg"></a>
											            
											            <strong><a href="http://www.amazon.com/gp/product/B00L6AW2MC/ref=as_li_tl?ie=UTF8&amp;camp=1789&amp;creative=9325&amp;creativeASIN=B00L6AW2MC&amp;linkCode=as2&amp;tag=dc03f3-20&amp;linkId=IC34E3JNCPW3DQGA">Ghost in the Shell 25th Anniversary [Blu-ray]</a><img src="http://ir-na.amazon-adsystem.com/e/ir?t=dc03f3-20&amp;l=as2&amp;o=1&amp;a=B00L6AW2MC" width="1" height="1" border="0" alt="" style="border:none !important; margin:0px !important;"></strong><br>
											         
													Director: Mamoro Oshii, 1995<br><br>
													IMDB Summary: A female cyborg cop and her partner hunt a mysterious and powerful hacker called the Puppet Master.


														</p>
														
														
														
														
														<hr>
														
														<p>
														<a title="Three Days of the Condor - poster"><img class="pullRight" src="assets/images/movies/condor.jpg"></a>
											            
											            <strong><a href="http://www.amazon.com/gp/product/B0036G1T78/ref=as_li_tl?ie=UTF8&amp;camp=1789&amp;creative=9325&amp;creativeASIN=B0036G1T78&amp;linkCode=as2&amp;tag=dc03f3-20&amp;linkId=Q4I3M7EZYSOYFQJE">Three Days of the Condor</a><img src="http://ir-na.amazon-adsystem.com/e/ir?t=dc03f3-20&amp;l=as2&amp;o=1&amp;a=B0036G1T78" width="1" height="1" border="0" alt="" style="border:none !important; margin:0px !important;"></strong><br>
											         
													Director: Sydney Pollack , 1975<br><br>
													IMDB Summary: A bookish CIA researcher finds all his co-workers dead, and must outwit those responsible until he figures out who he can really trust.


														</p>
														
														
														
														<hr>
														
														<p>
														<a title="The Conversation - poster"><img class="pullRight" src="assets/images/movies/conversation.jpg"></a>
											            
											            <strong><a href="http://www.amazon.com/gp/product/B003O7I6SE/ref=as_li_tl?ie=UTF8&amp;camp=1789&amp;creative=9325&amp;creativeASIN=B003O7I6SE&amp;linkCode=as2&amp;tag=dc03f3-20&amp;linkId=L25NUKVPSR6WY7XS">The Conversation</a><img src="http://ir-na.amazon-adsystem.com/e/ir?t=dc03f3-20&amp;l=as2&amp;o=1&amp;a=B003O7I6SE" width="1" height="1" border="0" alt="" style="border:none !important; margin:0px !important;"></strong><br>
											         
													Director: Francis Fnord Coppola , 1974<br><br>
													IMDB Summary: A paranoid, secretive surveillance expert has a crisis of conscience when he suspects that a couple he is spying on will be murdered.


														</p>
														
														<hr>
														
														<p>
														<a title="Enemy of the State - poster"><img class="pullRight" src="assets/images/movies/enemy.jpg"></a>
											            
											            <strong><a href="http://www.amazon.com/gp/product/B00KCGSRYI/ref=as_li_tl?ie=UTF8&amp;camp=1789&amp;creative=9325&amp;creativeASIN=B00KCGSRYI&amp;linkCode=as2&amp;tag=dc03f3-20&amp;linkId=UPLPNPQMZDTVZ6PX">Enemy Of The State</a><img src="http://ir-na.amazon-adsystem.com/e/ir?t=dc03f3-20&amp;l=as2&amp;o=1&amp;a=B00KCGSRYI" width="1" height="1" border="0" alt="" style="border:none !important; margin:0px !important;"></strong><br>
											         
